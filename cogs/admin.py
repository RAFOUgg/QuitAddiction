    # --- G√©n√©ration de la vue pour R√¥les et Salons avec pagination pour les salons ---
    def generate_general_config_view(self, guild_id: str, guild: discord.Guild) -> discord.ui.View:
        view = discord.ui.View(timeout=None)

        # Helper function to create options and a mapping
        def create_options_and_mapping(items, item_type):
            options = []
            id_mapping = {}

            if guild:
                sorted_items = sorted(items, key=lambda x: getattr(x, 'position', x.id))

                for item in sorted_items:
                    item_id = str(item.id)
                    item_name = item.name

                    if item_id is None or not isinstance(item_name, str) or not item_name:
                        print(f"DEBUG: Ignor√© item (type: {item_type}) car ID nul ou nom invalide: ID={item_id}, Nom={item_name}")
                        continue

                    # G√©n√©rer le label
                    label = item_name[:self.MAX_OPTION_LENGTH]
                    if not label:
                        label = item_id[:self.MAX_OPTION_LENGTH]
                        if not label:
                            print(f"DEBUG: Ignor√© item (type: {item_type}) car aucun label valide g√©n√©r√©: ID={item_id}, Nom='{item_name}'")
                            continue

                    # G√©n√©rer la value
                    hashed_id = hashlib.sha256(item_id.encode()).hexdigest()
                    value = hashed_id[:self.MAX_OPTION_LENGTH]
                    if not value:
                        print(f"DEBUG: Ignor√© item (type: {item_type}) car aucune value valide g√©n√©r√©e: ID={item_id}, Nom='{item_name}'")
                        continue

                    # V√©rification finale des longueurs avant d'ajouter
                    if not (1 <= len(label) <= 25 and 1 <= len(value) <= 25):
                        print(f"DEBUG: ERREUR DE LONGUEUR - Ignor√© item (type: {item_type})")
                        print(f"  -> Item original: ID='{item_id}', Nom='{item_name}'")
                        print(f"  -> Label g√©n√©r√© : '{label}' (longueur: {len(label)})")
                        print(f"  -> Value g√©n√©r√©e: '{value}' (longueur: {len(value)})")
                        continue # Ignorer si les longueurs ne sont pas bonnes malgr√© tout

                    options.append(discord.SelectOption(label=label, value=value, description=f"ID: {item_id}"))
                    id_mapping[value] = item_id

                if not options:
                    options.append(discord.SelectOption(label="Aucun trouv√©", value="no_items", description="Aucun item trouv√©.", default=True))
            else:
                options.append(discord.SelectOption(label="Erreur serveur", value="error_guild", description="Serveur non trouv√©.", default=True))
            
            return options, id_mapping

        # G√©n√©rer les options et le mapping pour les r√¥les
        role_options, role_id_mapping = create_options_and_mapping(guild.roles if guild else [], "role")

        # G√©n√©rer les options et le mapping pour les canaux textuels
        text_channels = [ch for ch in guild.channels if isinstance(ch, discord.TextChannel)] if guild else []
        channel_options_all, channel_id_mapping = create_options_and_mapping(text_channels, "channel")

        # Cr√©er les instances de Select pour les r√¥les
        # R√¥les : Admin (row=0), Notification (row=1)
        role_select_admin = self.RoleSelect(guild_id, "admin_role", row=0, options=role_options, id_mapping=role_id_mapping, cog=self) # Passer self
        view.add_item(role_select_admin)

        role_select_notif = self.RoleSelect(guild_id, "notification_role", row=1, options=role_options, id_mapping=role_id_mapping, cog=self) # Passer self
        view.add_item(role_select_notif)

        # --- Logique de pagination pour les salons ---
        # Ajouter les composants Select et Buttons directement √† la vue principale.

        # Cr√©er le Select pour les salons (row=0)
        channel_select_element = self.ChannelSelect(
            guild_id=guild_id,
            select_type="game_channel",
            row=0, # Select sur la ligne 0
            options=channel_options_all,
            id_mapping=channel_id_mapping,
            page=0, # Page initiale
            cog=self # Passer self
        )
        view.add_item(channel_select_element)

        # Cr√©er les boutons de navigation (row=1)
        if len(channel_options_all) > MAX_OPTIONS_PER_PAGE:
            prev_button = ui.Button(
                label="‚¨Ö Pr√©c√©dent",
                style=discord.ButtonStyle.secondary,
                custom_id=f"channel_prev_page_{guild_id}_0", # Inclure la page pour le callback
                disabled=True, # D√©sactiv√© au d√©but si page 0
                row=1 # Bouton pr√©c√©dent sur la ligne 1
            )
            next_button = ui.Button(
                label="Suivant ‚û°",
                style=discord.ButtonStyle.secondary,
                custom_id=f"channel_next_page_{guild_id}_0", # Inclure la page pour le callback
                disabled=False, # Activ√© s'il y a plus d'une page
                row=1 # Bouton suivant sur la ligne 1
            )
            # Il faut ici lier les callbacks aux boutons.
            # Comme nous n'avons plus de ChannelSelectView s√©par√©e, les callbacks des boutons doivent √™tre
            # impl√©ment√©s d'une autre mani√®re, soit en attenant un `ui.Button` avec son callback,
            # soit en utilisant une logique d'interaction globale (plus complexe).

            # Option la plus simple : cr√©er des instances de Button avec des callbacks associ√©s
            # et les ajouter √† la vue. Cela √©vite les probl√®mes avec les `@ui.button` et les `custom_id` dynamiques.
            
            # Red√©finissons ici les boutons avec leurs callbacks attach√©s directement.
            
            # Le `custom_id` est n√©cessaire pour que Discord sache √† quel √©l√©ment l'interaction appartient.
            # Les callbacks pour la navigation de page seront directement associ√©s √† ces boutons.

            # Le callback pour le bouton pr√©c√©dent
            async def prev_button_callback(interaction: discord.Interaction, guild_id=guild_id, current_page=0):
                if interaction.user.id != interaction.guild.owner_id: 
                    await interaction.response.send_message("Vous n'√™tes pas autoris√© √† changer de page.", ephemeral=True)
                    return
                
                # Il faut ici acc√©der √† l'√©tat de la page actuelle. Comme on ne passe pas la vue,
                # il faut trouver le moyen d'y acc√©der. La mani√®re la plus propre est de
                # ne pas ajouter les boutons directement ici, mais de les g√©rer via une vue parente
                # qui maintient l'√©tat.

                # OK, je vais r√©introduire ChannelSelectView pour g√©rer l'√©tat de la pagination.
                # La raison est que la vue principale (config_menu_view) n'a pas de moyen facile
                # de maintenir l'√©tat de la page pour le select de salon.

                # Retour √† l'id√©e initiale : ChannelSelectView g√®re les √©l√©ments pagin√©s.
                # Mais comment l'ajouter correctement √† la vue principale sans que les lignes se chevauchent ?

                # La solution est que ChannelSelectView contienne UNIQUEMENT le Select et les boutons de pagination,
                # et que ces √©l√©ments soient ajout√©s √† la vue principale avec leurs `row` appropri√©s.
                # Dans generate_general_config_view:
                # On cr√©e le Select (row=0)
